<template>
  <div class="view">
    <wlb-header :options="{ title: boardName }">
    </wlb-header>
    <textarea ref="textarea" class="topic-text-content" v-model="content" placeholder="请输入发帖内容"></textarea>
    <div class="topic-footer">
      <span class="residue">你还可以输入{{num}}字</span>
      <div @click="toPublic" :class="['btn', { disable: !publicEnd || !content }]"><span>{{publicEnd ? '发表' : '发表中...'}}</span></div>
    </div>
  </div>
</template>
<script type="text/javascript">
  import { WlbHeader } from '../components'
  import { wlb, api } from '../util'
  import { Toast } from 'mint-ui'
  export default {
    components: {
      WlbHeader
    },
    data () {
      return {
        active: true,
        title: '',
        content: '',
        num: 500,
        publicEnd: true,
        open: false,
        ThreadSectionList: [],
        boardName: ''
      }
    },
    created () {
      this.$plugin.loading.show(true, 'full')
      this.$http({
        url: this.$api.api_list,
        method: 'getBbsThreadSectionList',
        params: [{}]
      }).then((res) => {
        this.$plugin.loading.show(false)
        if (res.data.result.data) {
          for (let i in res.data.result.data) {
            this.ThreadSectionList[res.data.result.data[i]['id']] = res.data.result.data[i]
          }
          if (!this.ThreadSectionList[this.$route.params.id] || !this.ThreadSectionList[this.$route.params.id].name) {
            this.$router.replace({
              name: 'index'
            })
          } else {
            this.boardName = this.ThreadSectionList[this.$route.params.id].name
          }
        } else {
          this.$router.replace({
            name: 'index'
          })
        }
      })
      if (!this.$route.params.id) {
        this.$router.replace({
          name: 'index'
        })
      }
    },
    mounted () {
    },
    methods: {
      checkType (type) {
        this.active = type
        console.log(this.active)
      },
      toPublic () {
        this.content = this.content.toString()
        if (this.publicEnd === false || this.content === '') {
          return
        }
        this.publicEnd = false
        this.$http({
          url: this.$api.api_list,
          method: 'BbsPublishThread',
          params: [{
            type_id: this.$route.params.id,
            title: '网利社区',
            content: this.content
          }]
        }).then((res) => {
          this.publicEnd = true
          // this.content = ''
          if (res.data.result && res.data.result.code === 0) {
            // this.$rulemodal.show({ title: '系统提示', content: '帖子发布成功，已提交后台审核', style: 'text-align: center' })
            Toast({
              message: '帖子发布成功，已提交后台审核',
              duration: 1500
            })
            setTimeout(() => {
              // this.$rulemodal.show(false)
              this.$router.push({ name: 'index' })
            }, 1000)
          } else if (res.data.error && res.data.error.code === 4004) {
            // this.$rulemodal.show({ content: '用户未登录，请登陆后进行评论', style: 'text-align: center' })
            Toast({
              message: '用户未登录，请登陆后进行发帖',
              duration: 1500
            })
            setTimeout(() => {
              wlb.ready({
                app: function (mixins) {
                  mixins.loginApp({ refresh: 1, url: window.location.href + '' })
                },
                other: function () {
                  window.location.href = api.host + '/wechat/verify?next=' + window.location.href + '?source=app'
                }
              })
            }, 1000)
          } else {
            var msg = res.data.error.message
            // this.$rulemodal.show({ content: msg, style: 'text-align: center' })
            Toast({
              message: msg,
              duration: 1500
            })
            setTimeout(() => {
              // this.$rulemodal.show(false)
              this.$router.push({ name: 'index' })
            }, 1000)
          }
        })
      }
    },
    watch: {
      content () {
        var len = this.content.length
        this.num = 500 - len > 0 ? 500 - len : 0
        this.content = this.content.substring(0, 500)
      }
    }
  }
</script>
